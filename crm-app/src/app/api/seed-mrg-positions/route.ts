async function getOrCreateTag(name: string) {
  let tag = await prisma.tag.findFirst({ where: { name } })
  if (!tag) {
    tag = await prisma.tag.create({
      data: { name, type: 'POSITION', color: '#8b5cf6' }
    })
  }
  return tag
}

