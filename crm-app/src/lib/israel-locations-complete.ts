/**
 * נרמול מתקדם של שם יישוב - תמיכה בקיצורים, כתיבים שונים, טעויות כתיב
 */
export function normalizeLocalityComplete(name: string): string {
  if (!name) return ''
  
  let normalized = name.trim().toLowerCase()
  
  // הסרת גרשיים וגרש
  normalized = normalized.replace(/[""״׳']/g, '')
  
  // הסרת מקפים
  normalized = normalized.replace(/-/g, ' ')
  
  // נרמול רווחים
  normalized = normalized.replace(/\s+/g, ' ').trim()
  
  // בדיקת קיצורים
  for (const [alias, fullName] of Object.entries(EXTENDED_ALIASES)) {
    const aliasNorm = alias.toLowerCase().replace(/[""״׳'-]/g, '').replace(/\s+/g, ' ')
    if (normalized === aliasNorm || normalized.includes(aliasNorm)) {
      return fullName.toLowerCase()
    }
  }
  
  return normalized
}

/**
 * חילוץ חכם של יישוב מכתובת
 */
export function extractLocalityAdvanced(address: string): string {
  if (!address) return ''
  
  const normalized = address.toLowerCase()
  
  // ניסיון 1: חיפוש ישיר ברשימה המלאה
  for (const locality of ALL_ISRAEL_LOCALITIES) {
    const localityLower = locality.toLowerCase()
    if (normalized.includes(localityLower)) {
      return locality
    }
  }
  
  // ניסיון 2: פיצול לפי פסיקים (הערה האחרונה לרוב מכילה את העיר)
  const parts = address.split(',').map(p => p.trim()).reverse()
  for (const part of parts) {
    const partNorm = normalizeLocalityComplete(part)
    for (const locality of ALL_ISRAEL_LOCALITIES) {
      if (partNorm.includes(locality.toLowerCase()) || 
          locality.toLowerCase().includes(partNorm)) {
        return locality
      }
    }
  }
  
  // ניסיון 3: חיפוש קיצורים
  for (const [alias, fullName] of Object.entries(EXTENDED_ALIASES)) {
    if (normalized.includes(alias.toLowerCase())) {
      return fullName
    }
  }
  
  return normalizeLocalityComplete(address)
}

/**
 * בדיקת קרבה בין שני יישובים (30 דקות נסיעה)
 */
export function areLocationsProximate(loc1: string, loc2: string): boolean {
  if (!loc1 || !loc2) return false
  
  const norm1 = normalizeLocalityComplete(loc1)
  const norm2 = normalizeLocalityComplete(loc2)
  
  // אותו מקום
  if (norm1 === norm2) return true
  if (norm1.includes(norm2) || norm2.includes(norm1)) return true
  
  // בדיקה בקבוצות הקרובות
  for (const group of PROXIMITY_GROUPS) {
    const groupLower = group.map(c => normalizeLocalityComplete(c))
    const match1 = groupLower.some(c => norm1.includes(c) || c.includes(norm1))
    const match2 = groupLower.some(c => norm2.includes(c) || c.includes(norm2))
    if (match1 && match2) return true
  }
  
  return false
}

/**
 * קבלת כל היישובים הקרובים (30 דק)
 */
export function getProximateLocalities(locality: string): string[] {
  if (!locality) return []
  
  const normalized = normalizeLocalityComplete(locality)
  const nearby: string[] = [locality]
  
  for (const group of PROXIMITY_GROUPS) {
    const groupLower = group.map(c => normalizeLocalityComplete(c))
    if (groupLower.some(c => normalized.includes(c) || c.includes(normalized))) {
      nearby.push(...group)
    }
  }
  
  return Array.from(new Set(nearby))
}

