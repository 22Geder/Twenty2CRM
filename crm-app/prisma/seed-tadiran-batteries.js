// ==================== פונקציה ראשית ====================

async function main() {
  console.log('🚀 מתחיל להוסיף משרות תדיראן סוללות...')

  // יצירה/מציאה של המעסיק
  let employer = await prisma.employer.findFirst({
    where: { name: 'תדיראן סוללות' }
  })

  if (!employer) {
    employer = await prisma.employer.create({
      data: {
        name: 'תדיראן סוללות',
        email: EMPLOYER_EMAIL,
        phone: EMPLOYER_PHONE,
        website: 'https://www.tadiranbatteries.co.il',
        description: 'תדיראן סוללות - מפעל ייצור סוללות ומצברים מוביל בישראל, ממוקם בקרית מלאכי. מציע תנאי עבודה מעולים מהיום הראשון.'
      }
    })
    console.log('✅ נוצר מעסיק: תדיראן סוללות')
  } else {
    console.log('ℹ️ מעסיק תדיראן סוללות קיים - ממשיך ליצירת משרות')
  }

  let created = 0

  for (const pos of POSITIONS) {
    const tagsUnique = [...new Set(pos.tags)]
    const keywordsString = tagsUnique.join(', ')

    const transportationText = pos.hasTransportation
      ? `הסעות מ: ${TRANSPORTATION_CITIES}`
      : 'הגעה עצמאית (משרה בכירה)'

    const benefitsText = pos.hasTransportation
      ? `הסעות, חדר אוכל, שעות נוספות גבוהות, חופשה בחו"ל, מתנות בחגים, פנסיה וקרן השתלמות מהיום הראשון, 100% מחלה, דמי הבראה כפולים, פיצויים כפולים, בונוסים, שכר צמוד למדד, החזר רישוי רכב, קביעות + ביטוח בריאות הראל`
      : `חדר אוכל, שעות נוספות גבוהות, חופשה בחו"ל, מתנות בחגים, פנסיה וקרן השתלמות מהיום הראשון, 100% מחלה, דמי הבראה כפולים, פיצויים כפולים, בונוסים, שכר צמוד למדד, החזר רישוי רכב, קביעות + ביטוח בריאות הראל`

    await prisma.position.create({
      data: {
        title: pos.title,
        description: pos.description,
        requirements: pos.requirements,
        location: pos.location,
        salaryRange: pos.salaryRange,
        employmentType: pos.employmentType,
        active: true,
        employerId: employer.id,
        keywords: keywordsString,
        workHours: pos.workHours,
        transportation: transportationText,
        benefits: benefitsText,
      }
    })

    const icon = pos.hasTransportation ? '🚌' : '🚗'
    console.log(`  ${icon} נוצרה: ${pos.title}`)
    console.log(`      תגיות: ${tagsUnique.length} | הסעה: ${pos.hasTransportation ? 'כן' : 'לא'}`)
    created++
  }

  console.log(`\n✅ סה"כ נוצרו ${created} משרות לתדיראן סוללות!`)
  console.log('\n📋 סיכום:')
  console.log('  🚌 עם הסעות: מפעיל מכונה, מפעיל מתקן כימי, מבקר איכות, טכנאי חשמל ובקרים')
  console.log('  🚗 ללא הסעות (עצמאי): הנדסאי ציוד, מנהל אחזקת מכונות')
  console.log('\n⚠️  תזכורת: שכר לא מופיע בהודעות וואטסאפ – לשאול צפי שכר מהמועמד!')
}

main()
  .catch(e => {
    console.error('❌ שגיאה:', e)
    process.exit(1)
  })
  .finally(() => prisma.$disconnect())
