// פונקציה: מצא תגיות מתאימות לפי כותרת ושם מעסיק
// ──────────────────────────────────────────────────────────────

function getTagsForPosition(title, employerName) {
  const tags = new Set();

  // תגיות מעסיק
  for (const [empKey, empTags] of Object.entries(EMPLOYER_TAGS)) {
    if (employerName && employerName.includes(empKey)) {
      empTags.forEach(t => tags.add(t));
    }
  }

  // תגיות לפי כותרת
  const titleNormalized = title || '';

  if (titleNormalized.includes('יועץ שירות') || (titleNormalized.includes('יועץ') && titleNormalized.includes('שירות'))) {
    TAG_LIBRARY['יועץ שירות'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('דיאגנוסטיק')) {
    TAG_LIBRARY['דיאגנוסטיקה'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מכונאי') && titleNormalized.includes('ניידת') && titleNormalized.includes('צמ"ה')) {
    TAG_LIBRARY['צמה'].forEach(t => tags.add(t));
    TAG_LIBRARY['מכונאי ניידת'].forEach(t => tags.add(t));
  } else if (titleNormalized.includes('מכונאי') && titleNormalized.includes('ניידת')) {
    TAG_LIBRARY['מכונאי ניידת'].forEach(t => tags.add(t));
    TAG_LIBRARY['מכונאי'].forEach(t => tags.add(t));
  } else if (titleNormalized.includes('מכונאי') && titleNormalized.includes('צמ"ה')) {
    TAG_LIBRARY['צמה'].forEach(t => tags.add(t));
    TAG_LIBRARY['מכונאי'].forEach(t => tags.add(t));
  } else if (titleNormalized.includes('מכונאי') || titleNormalized.includes('מכונאות')) {
    TAG_LIBRARY['מכונאי'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('בוחן') && !titleNormalized.includes('בחינה')) {
    TAG_LIBRARY['בוחן'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מסגר')) {
    TAG_LIBRARY['מסגר'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('פחח')) {
    TAG_LIBRARY['פחח'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מחסנאי') || titleNormalized.includes('מחסן')) {
    TAG_LIBRARY['מחסנאי'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('נהג') || titleNormalized.includes('שינוע')) {
    TAG_LIBRARY['נהג'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('שכר') && !titleNormalized.includes('שכר מינימום')) {
    TAG_LIBRARY['שכר'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('חשבונות') || titleNormalized.includes('חשבונאי') || titleNormalized.includes('גזבר')) {
    TAG_LIBRARY['חשבונות'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('קופאי') || titleNormalized.includes('קופה')) {
    TAG_LIBRARY['קופאי'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('אופה') || titleNormalized.includes('אפיה') || titleNormalized.includes('אפייה') || titleNormalized.includes('מאפיה')) {
    TAG_LIBRARY['אופה'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מלצר') || titleNormalized.includes('מלצרת')) {
    TAG_LIBRARY['מלצר'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מכירות') || titleNormalized.includes('מכירן')) {
    TAG_LIBRARY['מכירות'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מנהל') || titleNormalized.includes('מנהלת')) {
    // בדוק שזה לא "מנהלת חשבונות" בלבד
    if (!titleNormalized.includes('חשבונות')) {
      TAG_LIBRARY['מנהל'].forEach(t => tags.add(t));
    } else {
      TAG_LIBRARY['חשבונות'].forEach(t => tags.add(t));
    }
  }
  if (titleNormalized.includes('לוגיסטיק') || titleNormalized.includes('תפעול')) {
    TAG_LIBRARY['לוגיסטיקה'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('חשמלאי')) {
    TAG_LIBRARY['חשמלאי'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('מזכיר') || titleNormalized.includes('פקיד') || titleNormalized.includes('אדמינ')) {
    TAG_LIBRARY['מזכיר'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('ביטוח')) {
    TAG_LIBRARY['ביטוח'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('בנק') || titleNormalized.includes('פיננסי')) {
    TAG_LIBRARY['בנקאי'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('גיוס') || titleNormalized.includes(' HR ') || titleNormalized === 'HR') {
    TAG_LIBRARY['גיוס'].forEach(t => tags.add(t));
  }
  if (titleNormalized.includes('IT') || titleNormalized.includes('מחשוב')) {
    TAG_LIBRARY['מחשבים'].forEach(t => tags.add(t));
  }

  return [...tags];
}

