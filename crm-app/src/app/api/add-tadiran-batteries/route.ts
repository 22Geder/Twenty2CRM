export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const key = searchParams.get('key');

    if (key !== ADMIN_KEY) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });
    }

    const results: string[] = [];

    // יצירה / מציאה של המעסיק
    let employer = await prisma.employer.findFirst({
      where: { name: 'תדיראן סוללות' }
    });

    if (!employer) {
      employer = await prisma.employer.create({
        data: {
          name: 'תדיראן סוללות',
          email: 'jobs@tadiranbatteries.co.il',
          phone: '08-9444461',
          website: 'https://www.tadiranbatteries.co.il',
          description: 'תדיראן סוללות - מפעל ייצור סוללות ומצברים מוביל בישראל, ממוקם בקריית עקרון. מציע תנאי עבודה מעולים מהיום הראשון.',
        }
      });
      results.push('✅ נוצר מעסיק: תדיראן סוללות');
    } else {
      results.push(`ℹ️ מעסיק קיים: ${employer.name}`);
    }

    let created = 0;
    let updated = 0;

    for (const pos of POSITIONS_DATA) {
      const tagsUnique = [...new Set(pos.tags)];

      const existing = await prisma.position.findFirst({
        where: { title: pos.title, employerId: employer.id }
      });

      if (existing) {
        await prisma.position.update({
          where: { id: existing.id },
          data: {
            description: pos.description,
            requirements: pos.requirements,
            location: pos.location,
            salaryRange: pos.salaryRange,
            employmentType: pos.employmentType,
            workHours: pos.workHours,
            transportation: pos.transportation,
            benefits: pos.benefits,
            keywords: tagsUnique.join(', '),
            active: true,
          }
        });
        results.push(`🔄 עודכנה: ${pos.title}`);
        updated++;
      } else {
        await prisma.position.create({
          data: {
            title: pos.title,
            description: pos.description,
            requirements: pos.requirements,
            location: pos.location,
            salaryRange: pos.salaryRange,
            employmentType: pos.employmentType,
            workHours: pos.workHours,
            transportation: pos.transportation,
            benefits: pos.benefits,
            keywords: tagsUnique.join(', '),
            active: true,
            employerId: employer.id,
          }
        });
        results.push(`✅ נוספה: ${pos.title} (${tagsUnique.length} תגיות)`);
        created++;
      }
    }

    const totalPositions = await prisma.position.count({
      where: { employerId: employer.id, active: true }
    });

    return NextResponse.json({
      success: true,
      employer: employer.name,
      results,
      summary: {
        created,
        updated,
        total: totalPositions,
      },
      note: '⚠️ שכר לא מוצג בוואטסאפ – יש לשאול צפי שכר מהמועמד',
    });

  } catch (error) {
    console.error('Error adding Tadiran positions:', error);
    return NextResponse.json({
      success: false,
      error: error instanceof Error ? error.message : 'Unknown error'
    }, { status: 500 });
  }
}
