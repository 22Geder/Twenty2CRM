export async function POST(request: NextRequest) {
  console.log('🚗 מעדכן משרות קבוצת UMI AVIS\n');

  try {
    // מציאת או יצירת מעסיק
    let employer = await prisma.employer.findFirst({
      where: { 
        OR: [
          { name: { contains: 'AVIS' } },
          { name: { contains: 'UMI' } }
        ]
      }
    });

    if (!employer) {
      console.log('⚠️ יוצר מעסיק קבוצת UMI AVIS...');
      employer = await prisma.employer.create({
        data: {
          name: 'קבוצת UMI AVIS',
          contactEmail: 'HasamaJobs@avis.co.il',
          contactName: 'מחלקת גיוס',
          phone: '',
          description: `קבוצת UMI AVIS - מובילה בתחום הרכב בישראל
• השכרת רכב
• ליסינג
• מכירת רכב חדש ומשומש
• שירותי צי
• ביטוח רכב

הטבות לכל התפקידים:
✅ עובד חברה מהיום הראשון
✅ שכר גבוה + עמלות
✅ אפשרויות קידום נרחבות
✅ ארוחות מסובסדות
✅ ביטוח בריאות לעובדים
✅ מתנות ואירועי רווחה
✅ ימי גיבוש`
        }
      });
    } else {
      // עדכון המייל אם קיים
      await prisma.employer.update({
        where: { id: employer.id },
        data: {
          contactEmail: 'HasamaJobs@avis.co.il',
          name: 'קבוצת UMI AVIS'
        }
      });
    }
    console.log(`✅ מעסיק: ${employer.name}`);

    // קבלת כל המשרות הקיימות
    const existingPositions = await prisma.position.findMany({
      where: { employerId: employer.id }
    });
    console.log(`📋 משרות קיימות: ${existingPositions.length}`);

    let created = 0;
    let updated = 0;

    // עדכון או יצירת משרות
    for (const pos of ALL_POSITIONS) {
      const existingPosition = existingPositions.find(p => 
        p.title === pos.title
      );

      if (existingPosition) {
        await prisma.position.update({
          where: { id: existingPosition.id },
          data: {
            title: pos.title,
            location: pos.location,
            description: pos.description,
            salaryRange: pos.salaryRange,
            employmentType: pos.employmentType,
            keywords: pos.keywords,
            active: true
          }
        });
        updated++;
        console.log(`🔄 עודכן: ${pos.title}`);
      } else {
        await prisma.position.create({
          data: {
            title: pos.title,
            location: pos.location,
            description: pos.description,
            salaryRange: pos.salaryRange,
            employmentType: pos.employmentType,
            keywords: pos.keywords,
            active: true,
            employerId: employer.id
          }
        });
        created++;
        console.log(`✨ נוצר: ${pos.title}`);
      }
    }

    const result = {
      success: true,
      message: `עדכון משרות קבוצת UMI AVIS הושלם בהצלחה!`,
      employer: employer.name,
      contactEmail: 'HasamaJobs@avis.co.il',
      stats: {
        created,
        updated,
        total: ALL_POSITIONS.length
      }
    };

    console.log('\n📊 סיכום:', result);
    return NextResponse.json(result);

  } catch (error) {
    console.error('❌ שגיאה:', error);
    return NextResponse.json(
      { success: false, error: String(error) },
      { status: 500 }
    );
  }
}

export async function GET() {
  return NextResponse.json({
    message: 'Use POST to sync AVIS UMI positions',
    contactEmail: 'HasamaJobs@avis.co.il',
    positionsCount: ALL_POSITIONS.length,
    positions: ALL_POSITIONS.map(p => ({ title: p.title, location: p.location, openPositions: p.openPositions }))
  });
}
