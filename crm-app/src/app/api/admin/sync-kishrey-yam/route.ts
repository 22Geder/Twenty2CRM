// POST handler
// ============================================================
export async function POST() {
  try {
    // 1. מצא או צור מעסיק
    let employer = await prisma.employer.findFirst({
      where: { name: { contains: 'קישרי', mode: 'insensitive' } },
    })

    if (!employer) {
      employer = await prisma.employer.create({
        data: {
          name: 'קישרי ים',
          email: 'jobs@kishrey-yam.co.il',
          description: 'קישרי ים – חברת כח אדם לעבודות נמל. לאשינג, סוורות ועבודות תפעוליות בנמל אשדוד.',
        },
      })
    }

    let created = 0
    let updated = 0

    // 2. צור / עדכן כל משרה
    for (const pos of POSITIONS) {
      const { tags, ...posData } = pos

      const tagConnects = {
        connectOrCreate: tags.map((tag: string) => ({
          where: { name: tag },
          create: { name: tag, color: '#0ea5e9' },
        })),
      }

      const existing = await prisma.position.findFirst({
        where: { title: pos.title, employerId: employer.id },
      })

      if (existing) {
        await prisma.position.update({
          where: { id: existing.id },
          data: {
            ...posData,
            active: true,
            employerId: employer.id,
            tags: tagConnects,
          },
        })
        updated++
      } else {
        await prisma.position.create({
          data: {
            ...posData,
            active: true,
            employerId: employer.id,
            tags: tagConnects,
          },
        })
        created++
      }
    }

    const totalActive = await prisma.position.count({ where: { active: true } })

    return NextResponse.json({
      success: true,
      employer: employer.name,
      created,
      updated,
      total: POSITIONS.length,
      totalActivePositions: totalActive,
      message: `קישרי ים סונכרן בהצלחה – ${created} נוצרו, ${updated} עודכנו`,
    })
  } catch (error) {
    console.error('sync-kishrey-yam error:', error)
    return NextResponse.json(
      { success: false, error: String(error) },
      { status: 500 }
    )
  }
}

