/**
 * חיפוש יישוב עם השלמה אוטומטית (fuzzy)
 */
export function searchLocality(query: string, limit: number = 10): string[] {
  if (!query || query.length < 2) return []
  
  const normalized = normalizeLocalityComplete(query)
  const results: string[] = []
  
  // התאמה מדויקת בהתחלה
  for (const locality of ALL_ISRAEL_LOCALITIES) {
    if (normalizeLocalityComplete(locality).startsWith(normalized)) {
      results.push(locality)
    }
  }
  
  // התאמה חלקית
  for (const locality of ALL_ISRAEL_LOCALITIES) {
    if (!results.includes(locality) && 
        normalizeLocalityComplete(locality).includes(normalized)) {
      results.push(locality)
    }
  }
  
  return results.slice(0, limit)
}

/**
 * קבלת סוג היישוב (עיר, קיבוץ, מושב, כפר)
 */
export function getLocalityType(locality: string): string {
  if (!locality) return 'לא ידוע'
  
  const normalized = normalizeLocalityComplete(locality)
  
  // ערים
  for (const city of [...ISRAELI_CITIES.major, ...ISRAELI_CITIES.medium, ...ISRAELI_CITIES.development]) {
    if (normalizeLocalityComplete(city) === normalized) return 'עיר'
  }
  
  // קיבוצים
  for (const arr of Object.values(KIBBUTZIM)) {
    for (const kib of arr) {
      if (normalizeLocalityComplete(kib) === normalized) return 'קיבוץ'
    }
  }
  
  // מושבים
  for (const arr of Object.values(MOSHAVIM)) {
    for (const mosh of arr) {
      if (normalizeLocalityComplete(mosh) === normalized) return 'מושב'
    }
  }
  
  // יישובים ערביים
  for (const arr of Object.values(ARAB_LOCALITIES)) {
    for (const arab of arr) {
      if (normalizeLocalityComplete(arab) === normalized) return 'יישוב ערבי'
    }
  }
  
  // יישובים דרוזיים
  for (const druze of DRUZE_LOCALITIES) {
    if (normalizeLocalityComplete(druze) === normalized) return 'יישוב דרוזי'
  }
  
  // שכונות
  for (const arr of Object.values(NEIGHBORHOODS)) {
    for (const neigh of arr) {
      if (normalizeLocalityComplete(neigh) === normalized) return 'שכונה'
    }
  }
  
  return 'יישוב'
}

