async function main() {
  console.log('🏷️  מעדכן תגיות לכל המשרות — מינימום 25 תגיות...\n');

  const positions = await prisma.position.findMany({
    include: { employer: true },
  });

  console.log(`📦 נמצאו ${positions.length} משרות בסך הכל\n`);

  let updated = 0;
  let skipped = 0;

  for (const pos of positions) {
    // פענוח keywords קיימים
    let existingTags = [];
    try {
      existingTags = pos.keywords ? JSON.parse(pos.keywords) : [];
      if (!Array.isArray(existingTags)) existingTags = [];
    } catch {
      existingTags = [];
    }

    // יצירת סט חדש עם הקיים + תגיות חכמות
    const combined = new Set(existingTags.map(t => String(t).trim()).filter(Boolean));
    const employerName = pos.employer?.name || '';

    // הוסף תגיות חכמות לפי תפקיד
    const smartTags = getTagsForPosition(pos.title, employerName);
    smartTags.forEach(t => combined.add(t));

    // אם עדיין פחות מ-25, הוסף תגיות גנריות
    let genericIdx = 0;
    while (combined.size < 25 && genericIdx < GENERIC_TAGS.length) {
      combined.add(GENERIC_TAGS[genericIdx]);
      genericIdx++;
    }

    const finalTags = [...combined];

    // בדוק אם שינינו משהו
    const before = JSON.stringify(existingTags.sort());
    const after = JSON.stringify(finalTags.slice().sort());

    if (before === after && existingTags.length >= 25) {
      skipped++;
      continue;
    }

    // עדכון
    await prisma.position.update({
      where: { id: pos.id },
      data: { keywords: JSON.stringify(finalTags) },
    });
    updated++;
    console.log(`✅ [${pos.employer?.name}] ${pos.title} — ${finalTags.length} תגיות`);
  }

  console.log(`\n🎉 סיום!`);
  console.log(`   עודכנו: ${updated} משרות`);
  console.log(`   ללא שינוי: ${skipped} משרות`);
}

main()
  .catch(e => {
    console.error('❌ שגיאה:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
