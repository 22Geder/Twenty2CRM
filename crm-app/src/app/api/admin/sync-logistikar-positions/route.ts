export async function POST() {
  try {
    // 1. מצא/צור מעסיק
    let employer = await prisma.employer.findFirst({
      where: {
        OR: [
          { name: { contains: 'לוגיסטיקר', mode: 'insensitive' } },
          { name: { contains: 'לוגיסטקר', mode: 'insensitive' } },
        ],
      },
    })

    if (!employer) {
      employer = await prisma.employer.create({
        data: {
          name: 'לוגיסטיקר',
          description: 'לוגיסטיקר - שירותי לוגיסטיקה והפצה',
          active: true,
        },
      })
    }

    // 2. הקפא את כל המשרות הקיימות
    const frozenResult = await prisma.position.updateMany({
      where: { employerId: employer.id },
      data: { active: false },
    })

    // 3. צור/עדכן רק את המשרות העדכניות
    const results: { action: string; title: string; location: string }[] = []

    for (const pos of POSITIONS) {
      const existing = await prisma.position.findFirst({
        where: { employerId: employer.id, title: pos.title, location: pos.location },
      })

      if (existing) {
        await prisma.position.update({
          where: { id: existing.id },
          data: { ...pos, active: true },
        })
        results.push({ action: 'updated', title: pos.title, location: pos.location })
      } else {
        await prisma.position.create({
          data: { ...pos, employerId: employer.id, active: true, openings: 1, priority: 5 },
        })
        results.push({ action: 'created', title: pos.title, location: pos.location })
      }
    }

    const activeCount = await prisma.position.count({ where: { employerId: employer.id, active: true } })
    const inactiveCount = await prisma.position.count({ where: { employerId: employer.id, active: false } })
    const totalAllActive = await prisma.position.count({ where: { active: true } })

    return NextResponse.json({
      success: true,
      employer: employer.name,
      frozenPrevious: frozenResult.count,
      processed: results.length,
      activeNow: activeCount,
      frozenNow: inactiveCount,
      totalActiveInSystem: totalAllActive,
      positions: results,
    })
  } catch (error: any) {
    console.error('sync-logistikar error:', error)
    return NextResponse.json({ error: error.message }, { status: 500 })
  }
}
